public class Info {
    private int length;
    private int width;
    private int cost;

    public int getLength() {
        return length;
    }

    public int getWidth() {
        return width;
    }

    public int getCost() {
        return cost;
    }

    public Info(int length, int width, int cost) {
        this.length = length;
        this.width = width;
        this.cost = cost;
    }
}
